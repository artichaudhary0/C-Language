/*
    D A T A   T Y P E S :
    Data type defines the type of data will store.
    Each variable in C has an associated data type.
    It specifies the type of data that the variable can store like integer, character, floating, double, etc.
    Each data type requires different amounts of memory and has some specific operations which can be performed over it. 
    The data type is a collection of data with values having fixed values, meaning as well as its characteristics.
    
    THERE ARE THREE DATA TYPES :
    1: PRIMITIVE DATA TYPES.
    2: DERIVED DATA TYPES.
    3: USER DEFINE DATA TYPE.

    P R I M I T I V E   D A T A   T Y P E S.
    Primitive data types are the most basic data types that are used for representing 
    simple values.
    Example : integers, float, characters, etc.


    D E R I V E D   D A T A   T Y P E S.
    The data types that are derived from the primitive or built-in datatypes are 
    referred to as Derived Data Types.
    Example : string, array, pointer, function.


    U S E R   D E F I N E   D A T A   T Y P E.
    The user-defined data types are defined by the user himself.
    Example : Structure, enum, union, class, typedef.
*/